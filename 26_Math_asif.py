# Math
# Built-in Math Functions

x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

# Another example

x = abs(-7.25)

print(x)

# Another example

x = pow(4, 3)

print(x)

# The Math Module

import math

# Another example

x = math.sqrt(64)

print(x)

# Another example

x = math.ceil(1.4)

y = math.floor(1.4)

print(x) # returns 2
print(y) # returns 1

